    public String getLetzterKurs() {
        return letzterKurs;
    }

    public void setLetzterKurs(String letzterKurs) {
        this.letzterKurs = letzterKurs;
    }

    public String getVeraenderung() {
        return veraenderung;
    }

    public void setVeraenderung(String veraenderung) {
        this.veraenderung = veraenderung;
    }
